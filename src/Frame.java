    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal=dateFormat.format(berangkat.getDate());
        
        if ("".equals(id.getText())||"".equals(nama.getText())||"".equals(awal.getItemListeners())||"".equals(tujuan.getItemListeners())||"".equals(jnskereta.getItemListeners())||tanggal.equals("")||"".equals(waktu.getItemListeners())||"".equals(nogerbong.getItemListeners()) ||"".equals(noduduk.getItemListeners())|| "".equals(harga.getText())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error",JOptionPane.WARNING_MESSAGE);
        } else {
            String SQL ="INSERT INTO `pesanan`(`ktpsim`, `nama`, `sta_awal`, `sta_tujuan`, `jnskereta`, `tgl_berangkat`, `wkt_berangkat`, `nogerbong`, `jmltempat`, `harga`)" 
                    + "VALUES("+id.getText()+",'"+nama.getText()+"','"+(String)awal.getSelectedItem()+"','"+(String)tujuan.getSelectedItem()+"','"+(String)jnskereta.getSelectedItem()+"','"+tanggal+"',"+(String)waktu.getSelectedItem()+","+(String)nogerbong.getSelectedItem()+","+(String)noduduk.getSelectedItem()+",'"+harga.getText()+"')";
            int status = KonekDB.execute(SQL);
            if (status==1) {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan","Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan","Gagal",JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_saveActionPerformed

