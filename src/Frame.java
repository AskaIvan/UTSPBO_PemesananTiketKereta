    private void selectData() {
        String kolom[] = {"KTPSIM","Nama","StasiunAwal","StasiunTujuan","JenisKereta",
            "TglBerangkat","WaktuBerangkat","NoGerbong","JmlTempat","Harga"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM pesanan";
        ResultSet rs = KonekDB.executeQuery(SQL);
        try {
            while (rs.next()) {                
                String KTPSIM = rs.getString(1);
                String Nama = rs.getString(2);
                String StasiunAwal = rs.getString(3);
                String StasiunTujuan = rs.getString(4);
                String JenisKereta = rs.getString(5);
                String TglBerangkat = rs.getString(6);
                String WaktuBerangkat = rs.getString(7);
                String NoGerbong = rs.getString(8);
                String JmlTempat = rs.getString(9);
                String Harga = rs.getString(10);
                String data[] = {KTPSIM,Nama,StasiunAwal,StasiunTujuan,JenisKereta,TglBerangkat,WaktuBerangkat,NoGerbong,JmlTempat,Harga};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblData.setModel(dtm);
    }
