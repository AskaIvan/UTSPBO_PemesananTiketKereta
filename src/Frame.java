    private void jnskeretaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jnskeretaActionPerformed
        // TODO add your handling code here:
        String rego="";
        int penumpang = Integer.valueOf((String)noduduk.getSelectedItem());
        int ekonomi = 40000*penumpang;
        int bisnis = 50000*penumpang;
        int eksekutif = 60000*penumpang;
        if(jnskereta.getSelectedItem().toString()=="Ekonomi"){
            rego= String.valueOf(ekonomi);
            harga.setText(rego);
        }else if(jnskereta.getSelectedItem().toString()=="Bisnis"){
            rego= String.valueOf(bisnis);
            harga.setText(rego);
        }else if(jnskereta.getSelectedItem().toString()=="Eksekutif"){
            rego= String.valueOf(eksekutif);
            harga.setText(rego);
        }else{
            JOptionPane.showMessageDialog(this, "Pilih Jenis Kereta","Gagal",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jnskeretaActionPerformed

